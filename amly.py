def main():
    parser = argparse.ArgumentParser(
        description=ASCII_ART,
        formatter_class=argparse.RawDescriptionHelpFormatter
    )
    parser.add_argument(
        '-v',
        '--version',
        action='version',
        version='%(prog)s v1.0.0'
    )
    parser.add_argument(
        '-s',
        '--sync',
        help="Save timecode's in 00:00.000 format (three ms points)",
        action="store_true"
    )
    parser.add_argument(
        'urls',
        help="Apple Music URLs for albums or songs",
        type=str,
        nargs='*'
    )
    args = parser.parse_args()

    if not args.urls and not args.sync:
        logger.warning("Don't run this by double-clicking the exe. Use command line arguments instead!")
        parser.print_usage()
        return
    
    handler(args)

if __name__ == "__main__":
    print(LOGO)
    main()
